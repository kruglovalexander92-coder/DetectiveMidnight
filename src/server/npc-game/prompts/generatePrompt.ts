import { config } from '../config.js';
import type { ChatMessage } from '../../llm/types.js';

export function generatePromptMessages(): ChatMessage[] {
	return [
		{
			role: 'system',
			content: `Ты — генератор персонажей для текстовой игры-симулятора общения. Создай одного NPC (неигрового персонажа) в строгом формате JSON. Весь текст NPC (имя, описание, приветствие, тема интереса, подсказки желаний, секрет) должен быть на языке с кодом "${config.language}" (русский, если ru).

Правила создания NPC:
- name: яркое, запоминающееся имя.
- description: краткое описание внешности и характера (1-2 предложения).
- greeting: первая реплика NPC игроку, строго в образе персонажа.
- personality: четыре черты, каждая — число от 0.0 до 1.0:
  - temper (вспыльчивость): выше = быстрее обижается, резче реагирует на грубость.
  - aggressiveness (агрессивность): выше = грубая/угрожающая речь, большие штрафы.
  - romanticness (романтичность): выше = тёплый/флиртующий тон, бонусы за комплименты.
  - education (образованность): выше = ценит грамотность и знания, суровее к невежеству.
- interest: тема, которой NPC увлечён и о которой говорит в основном (topic + description).
- desires: массив из 1-3 желаний; каждое содержит item (точное название предмета) и hint (загадочная подсказка, как NPC намекает на желание).
- secret: личный секрет, который NPC раскроет только при максимальном уровне отношений.
- inventory: массив из 4-5 предметов для игрока. С вероятностью ~60% включи ОДИН предмет, ТОЧНО совпадающий по написанию с item одного из желаний NPC. Остальные — отвлекающие предметы.
- caseAnswers: ответы NPC на 4 вопроса по делу расследования. Каждый ответ должен быть в образе персонажа, отражать его характер и содержать конкретную информацию:
  - hair: описание причёски или головного убора подозреваемого (1-2 предложения с деталями).
  - eyes: описание глаз, очков или особых примет в области глаз (1-2 предложения с деталями).
  - mustache: описание усов, бороды или отсутствия растительности на лице (1-2 предложения с деталями).
  - skin: описание тона кожи подозреваемого (1-2 предложения с деталями).
- photoFeatures: enum-значения для фоторобота подозреваемого. Каждое значение должно быть ОДНИМ из указанных вариантов:
  - hair: 'bald' (лысый) | 'short' (короткая стрижка) | 'curly' (кудри) | 'tophat' (цилиндр/шляпа)
  - eyes: 'glasses' (очки) | 'angry' (злой взгляд) | 'normal' (обычные глаза) | 'monocle' (монокль)
  - mustache: 'none' (нет растительности) | 'gentleman' (аккуратные усы) | 'beard' (борода) | 'pirate' (длинная борода/усы)
  - skin: 'pale' (бледная) | 'tanned' (загорелая) | 'fair' (светлая)

Верни ТОЛЬКО валидный JSON строго такой структуры (без markdown, без пояснений):
{
  "npc": {
    "name": "...",
    "description": "...",
    "greeting": "...",
    "personality": { "temper": 0.0, "aggressiveness": 0.0, "romanticness": 0.0, "education": 0.0 },
    "interest": { "topic": "...", "description": "..." },
    "desires": [ { "item": "...", "hint": "..." } ],
    "secret": "...",
    "caseAnswers": {
      "hair": "...",
      "eyes": "...",
      "mustache": "...",
      "skin": "..."
    },
    "photoFeatures": {
      "hair": "bald|short|curly|tophat",
      "eyes": "glasses|angry|normal|monocle",
      "mustache": "none|gentleman|beard|pirate",
      "skin": "pale|tanned|fair"
    }
  },
  "inventory": ["...", "...", "..."]
}`,
		},
		{
			role: 'user',
			content: 'Создай нового NPC и инвентарь игрока. Сделай персонажа колоритным и интересным для диалога.',
		},
	];
}
